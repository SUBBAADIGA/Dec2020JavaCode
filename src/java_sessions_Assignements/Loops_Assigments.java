package java_sessions_Assignements;

public class Loops_Assigments {

	public static void main(String[] args) {
		
		for(int i=0;i<5;i++)
		{
			System.out.println("I am Batman");
		}
		
		System.out.println("---------------------------");
		for(int i=0;i<5;i++)
		{
			System.out.println("I am Batman"+" "+ i);
		}

	}

}

